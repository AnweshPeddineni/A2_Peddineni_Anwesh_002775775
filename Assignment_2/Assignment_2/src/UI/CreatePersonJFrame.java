   private void loadComboBoxData(){
        cmbCity.removeAllItems();
        cmbCommunity.removeAllItems();
        cmbHospital.removeAllItems();
        
          for(City city: SystemAdmin.citylist){
               cmbCity.addItem(city.getCity());
          }
           
          for(Community community: SystemAdmin.communitylist){
              cmbCommunity.addItem(community.getCommunity());
          }
          
          for(Hospital hospital: SystemAdmin.hospitallist){
              cmbHospital.addItem(hospital.getHospital());
          }
          
          cmbCity.setSelectedIndex(-1);
          cmbCommunity.setSelectedIndex(-1);
          cmbHospital.setSelectedIndex(-1);


    }

