    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText();
        int id = Integer.parseInt(txtPatientId.getText());
        int age = Integer.parseInt(txtAge.getText());
        String gender = Gender;
        String address = txtAddress.getText();
        String city = (String)cmbCity.getSelectedItem();
        String community = (String)cmbCommunity.getSelectedItem();
        long cellphoneno = Long.parseLong(txtCellphoneNo.getText());
        String emailid = txtEmailId.getText();
        String hospital = (String)cmbHospital.getSelectedItem();
        String doctor = (String)cmbDoctor.getSelectedItem();
        
        PersonDetails pd = new PersonDetails();

        House h = new House();
        h.setAddress(address);
        
        Community co = new Community();
        co.setCommunity(community);
        
        City ci = new City();
        ci.setCity(city);
        
        Hospital ho = new Hospital();
        ho.setHospital(hospital);
        
        DoctorDetails doc = new DoctorDetails();
        doc.setDoctor(doctor);

        pd.setName(name);
        pd.setPatientId(id);
        pd.setAge(age);
        pd.setGender(gender);
        pd.setCellPhoneNumber(cellphoneno);
        pd.setEmailID(emailid);
        
        list.addnewperson(pd);
        
        JOptionPane.showMessageDialog(this, "Person Details Submitted Successfully!!");
      
      txtName.setText("");
      txtPatientId.setText("");
      txtAge.setText("");
      txtAddress.setText("");
      cmbCity.setSelectedIndex(-1);
      cmbCommunity.setSelectedIndex(-1);
      txtCellphoneNo.setText("");
      txtEmailId.setText("");
      cmbHospital.setSelectedIndex(-1);
      cmbDoctor.setSelectedIndex(-1);
        
    }//GEN-LAST:event_btnSaveActionPerformed

